# stringr_package 

# stringr 패키지에서 패턴을 찾는 메소드 -> 4가지가 존재 

# str_detect, str_count, str_which, str_locate 

## str_detect
# string중 해당 정규식에 맞는 문자열이 총 몇개 존재하는지 반환하는 메소드 


## str_count 
# 정규식에 맞는 문자열이 총 몇개 존재하는지 반환하는 메소드 


## str_which
# 정규식에 맞는 문자열이 앞에서 부터 몇번째 있는지 반환합니다.
# 만약 해당 패턴이 없는 경우는 아무것도 반환하지 않는 메소드


## str_locate
# 정규식에 맞는 문자열의 시작점과 종료 지점을 반환하는 메소드 
